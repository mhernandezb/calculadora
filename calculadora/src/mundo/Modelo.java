public class Modelo {
    double numero1;
    double numero2;

    public double sumar(double num1, double num2){
        return num1+num2;
    }
    public double restar(double num1, double num2){
        return num1-num2;
    }
    public double multiplicar(double num1, double num2){
        return num1*num2;
    }
    public double dividir(double num1, double num2){
        return num1/num2;
    }

}
